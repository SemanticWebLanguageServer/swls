//! Node.js `node_modules` directory traversal.
//!
//! Discovers every npm package directory reachable from the project root by following Node's
//! module resolution algorithm: walk up the directory tree collecting `node_modules/` entries,
//! descend into each package directory (including scoped `@scope/pkg` packages), and avoid
//! revisiting the same canonical URL twice.

use std::collections::HashSet;

use url::Url;

use crate::error::Result;
use crate::fs::Fs;

/// Build all ancestor directory URLs from the main module URL, to be used as
/// starting points for node_modules scanning.
/// Mirrors `ModuleStateBuilder.buildNodeModuleImportPaths`.
///
/// `main_module_path` must be a directory URL (ending with `/`).
pub fn build_node_module_import_paths(main_module_path: &Url) -> Vec<Url> {
    let mut paths = Vec::new();
    let mut current = main_module_path.clone();
    paths.push(current.clone());
    loop {
        let parent = match current.join("..") {
            Ok(p) => p,
            Err(_) => break,
        };
        if parent == current {
            break;
        }
        current = parent;
        paths.push(current.clone());
    }
    paths
}

/// Recursively discover all node module paths starting from the given import URLs.
/// Mirrors `ModuleStateBuilder.buildNodeModulePaths`.
pub async fn build_node_module_paths(
    fs: &dyn Fs,
    import_paths: &[Url],
) -> Result<Vec<Url>> {
    let mut result = Vec::new();
    let mut visited = HashSet::new();
    for path in import_paths {
        build_node_module_paths_inner(fs, path, &mut result, &mut visited).await?;
    }
    Ok(result)
}

async fn build_node_module_paths_inner(
    fs: &dyn Fs,
    path: &Url,
    result: &mut Vec<Url>,
    visited: &mut HashSet<Url>,
) -> Result<()> {
    let canonical = match fs.canonicalize(path).await {
        Ok(p) => p,
        Err(_) => return Ok(()),
    };

    if visited.contains(&canonical) {
        return Ok(());
    }
    visited.insert(canonical.clone());

    let pkg_json_url = match canonical.join("package.json") {
        Ok(u) => u,
        Err(_) => return Ok(()),
    };
    let node_modules_url = match canonical.join("node_modules/") {
        Ok(u) => u,
        Err(_) => return Ok(()),
    };

    let has_package_json = fs.is_file(&pkg_json_url).await;
    let has_node_modules = fs.is_dir(&node_modules_url).await;

    if has_package_json || has_node_modules {
        result.push(canonical.clone());

        if has_node_modules {
            if let Ok(entries) = fs.read_dir(&node_modules_url).await {
                for entry in entries {
                    if entry.name.starts_with('.') {
                        continue;
                    }

                    if entry.name.starts_with('@') {
                        // Scoped packages: iterate one level deeper
                        if let Ok(scoped_entries) = fs.read_dir(&entry.path).await {
                            for scoped_entry in scoped_entries {
                                Box::pin(build_node_module_paths_inner(
                                    fs,
                                    &scoped_entry.path,
                                    result,
                                    visited,
                                ))
                                .await?;
                            }
                        }
                    } else {
                        Box::pin(build_node_module_paths_inner(
                            fs,
                            &entry.path,
                            result,
                            visited,
                        ))
                        .await?;
                    }
                }
            }
        }
    }

    Ok(())
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_build_import_paths() {
        let root = Url::from_directory_path("/a/b/c").unwrap();
        let paths = build_node_module_import_paths(&root);
        assert_eq!(
            paths,
            vec![
                Url::from_directory_path("/a/b/c").unwrap(),
                Url::from_directory_path("/a/b").unwrap(),
                Url::from_directory_path("/a").unwrap(),
                Url::from_directory_path("/").unwrap(),
            ]
        );
    }
}
